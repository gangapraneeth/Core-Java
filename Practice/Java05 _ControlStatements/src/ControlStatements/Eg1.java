package ControlStatements;

public class Eg1 {
	
	//Types Of Statements:
	//1.Selection Statements : if, else, else-if, switch
	//2.Iteration Statements : for, while, do-while;
	//3.Transfer Statements : break, continue

	public static void main(String[] args) {
		
		// Selection Statements:
		
		if(true) {
			System.out.println("if condition 1");
		}
		else {
			System.out.println("else block 1");
		}
		
		if(false) {
			System.out.println("if condition 2");
		}
		else {
			System.out.println("else block 2");
		}
		
		if(true) {
			System.out.println("if condition 3");
		}
		else {
			System.out.println("else block 3");
		}
		

	}

}
